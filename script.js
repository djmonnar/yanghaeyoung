const $ = (selector) => document.querySelector(selector);
const list = (items) => `<ul class="list">${items.map((item) => `<li>${item}</li>`).join("")}</ul>`;
const image = (item) => `<img src="${item.path}" alt="${item.alt}" loading="lazy" onerror="this.onerror=null;this.src='${item.fallback}'" />`;

function renderHeader() {
  const menu = $("#site-menu");
  menu.innerHTML = navItems.map(([label, href]) => `<a href="${href}">${label}</a>`).join("");
  const button = $(".menu-button");
  button.addEventListener("click", () => {
    const open = menu.classList.toggle("is-open");
    button.setAttribute("aria-expanded", String(open));
  });
  menu.addEventListener("click", () => {
    menu.classList.remove("is-open");
    button.setAttribute("aria-expanded", "false");
  });
}

